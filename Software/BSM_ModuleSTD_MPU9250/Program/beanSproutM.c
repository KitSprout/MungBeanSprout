#include "drivers\stm32f0_system.h"
#include "modules\module_serial.h"
#include "modules\module_mpu9250.h"

#include "beanSproutM.h"
/*=====================================================================================================*/
/*=====================================================================================================*/
void BSM_Init( void )
{
  SystemInit();
  HAL_InitTick();

  BSM_GPIO_Config();
  BSM_UART_Config(NULL);
  BSM_MPU9250_Config();
}
/*=====================================================================================================*/
/*=====================================================================================================*/
void BSM_Loop( void )
{
  int16_t imu[10] = {0};

  while(1) {
    MPU9250_getData(imu);
    printf("AX:%5i\tAY:%5i\tAZ:%5i\tGX:%5i\tGY:%5i\tGZ:%5i\tMX:%5i\tMY:%5i\tMZ:%5i\r\n", imu[1], imu[2], imu[3], imu[4], imu[5], imu[6], imu[7], imu[8], imu[9]);
    delay_ms(100);
  }
}
