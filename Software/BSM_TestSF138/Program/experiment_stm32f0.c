#include "stm32f0_system.h"
#include "experiment_stm32f0.h"
#include "module_sf138.h"
/*====================================================================================================*/
/*====================================================================================================*/
int main( void )
{
  uint8_t i = 0;
  uint8_t dir = 0;

  SF138_Config();

  while(1) {
    SF138_WriteData(i);
    Delay_10ms(5);

    switch(dir) {
      case 0:   if(++i == 7)  dir = 1;  break;
      case 1:   if(--i == 0)  dir = 0;  break;
    }
  }
}
